int gridX, gridY;

void initGrid(int x, int y)
{
	gridX = x;
	gridY = y;
}

void square(int x, int y) // Function to draw a square
{
    glBegin(GL_QUADS); 
    glVertex2f(x, y);
    glVertex2f(x + 1, y);
    glVertex2f(x + 1, y + 1);
    glVertex2f(x, y + 1);
    glEnd(); 
}

void drawGrid()
{
	for (int x = 0; x < gridX; x++)
	{
		for (int y = 0; y < gridY; y++)
		{
			if (maze[x][y] == 1)
			{
				glColor3f(0.261, 0.416, 0.437);
				square(x, y);
			}
		}
	}
}

